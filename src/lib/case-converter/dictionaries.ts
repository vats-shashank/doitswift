/** Sorted longest-first for phrase replacement. */
function sortedProperPairs() {
  return [...PROPER_NOUNS].sort((a, b) => b[0].length - a[0].length);
}

let _sorted: [string, string][] | null = null;
export function getSortedProper() {
  if (!_sorted) _sorted = sortedProperPairs();
  return _sorted;
}
